    function get_menu($menuload, $seccion) {

        $out = "";

        foreach ($menuload[$seccion] as $item) {
            $out .= '<li><a class="gac" data-category="' . $item["category"] . '" data-action="' . $item["action"] . '" data-opt_label="' . $item["opt_label"] . '"  href="' . $item["link"] . '" title="' . $item["title"] . '" rel="' . $item["rel"] . '">' . $item["title"] . '</a></li>';
        }

